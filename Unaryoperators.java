  import java.io.*;
  public class Unaryoperators { 
    public static void main(String[] args) {
        int a=10;
        int b=10;

        System.out.println("preincrement:"+(++a));
        System.out.println("postincrement:"+(a++));

        System.out.println("predecrement:"+(--b));
        System.out.println("postdecrement:"+(b--));


    } 
    
}
